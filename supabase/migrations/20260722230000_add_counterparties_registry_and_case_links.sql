create or replace function public.set_case_counterparties(
  p_case_id bigint,
  p_counterparty_ids bigint[] default '{}'::bigint[]
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  current_studio_id uuid;
  current_counterparty_id bigint;
  normalized_ids bigint[];
  existing_link_id bigint;
begin
  if auth.uid() is null then
    raise exception 'Utente non autenticato.' using errcode = '42501';
  end if;

  select current_case.studio_id
  into current_studio_id
  from public.cases as current_case
  join public.profiles as current_profile
    on current_profile.studio_id = current_case.studio_id
   and current_profile.id = auth.uid()
   and current_profile.active = true
   and current_profile.deleted_at is null
  join public.user_permissions as current_permissions
    on current_permissions.user_id = current_profile.id
   and current_permissions.can_edit_cases = true
  where current_case.id = p_case_id
    and current_case.deleted_at is null;

  if current_studio_id is null then
    raise exception 'Pratica non trovata o modifica non autorizzata.'
      using errcode = '42501';
  end if;

  select coalesce(array_agg(distinct requested_id), '{}'::bigint[])
  into normalized_ids
  from unnest(coalesce(p_counterparty_ids, '{}'::bigint[]))
    as requested(requested_id)
  where requested_id is not null;

  if exists (
    select 1
    from unnest(normalized_ids) as requested(requested_id)
    left join public.counterparties as requested_counterparty
      on requested_counterparty.id = requested_id
     and requested_counterparty.studio_id = current_studio_id
     and (
       requested_counterparty.deleted_at is null
       or exists (
         select 1
         from public.case_counterparties as historical_link
         where historical_link.case_id = p_case_id
           and historical_link.counterparty_id = requested_id
           and historical_link.deleted_at is null
       )
     )
    where requested_counterparty.id is null
  ) then
    raise exception
      'Una o più controparti non esistono, sono eliminate o appartengono a un altro studio.'
      using errcode = '22023';
  end if;

  update public.case_counterparties
  set
    deleted_at = now(),
    deleted_by = auth.uid(),
    delete_reason = 'Scollegata dalla pratica'
  where case_id = p_case_id
    and studio_id = current_studio_id
    and deleted_at is null
    and not (counterparty_id = any(normalized_ids));

  foreach current_counterparty_id in array normalized_ids
  loop
    select existing_link.id
    into existing_link_id
    from public.case_counterparties as existing_link
    where existing_link.case_id = p_case_id
      and existing_link.counterparty_id = current_counterparty_id
    order by
      (existing_link.deleted_at is null) desc,
      existing_link.created_at desc,
      existing_link.id desc
    limit 1;

    if existing_link_id is null then
      insert into public.case_counterparties (
        studio_id,
        case_id,
        counterparty_id,
        created_by
      )
      values (
        current_studio_id,
        p_case_id,
        current_counterparty_id,
        auth.uid()
      );
    else
      update public.case_counterparties
      set
        deleted_at = null,
        deleted_by = null,
        delete_reason = null
      where id = existing_link_id;
    end if;

    existing_link_id := null;
  end loop;
end;
$$;

revoke all on function public.set_case_counterparties(bigint, bigint[])
  from public;
grant execute on function public.set_case_counterparties(bigint, bigint[])
  to authenticated;

create or replace function public.save_case_with_counterparties(
  p_case jsonb,
  p_counterparty_ids bigint[] default '{}'::bigint[],
  p_case_id bigint default null
)
returns bigint
language plpgsql
security invoker
set search_path = public
as $$
declare
  authorized_studio_id uuid;
  requested_studio_id uuid;
  requested_client_id bigint;
  existing_client_id bigint;
  saved_case_id bigint;
begin
  if auth.uid() is null then
    raise exception 'Utente non autenticato.' using errcode = '42501';
  end if;

  if p_case is null or jsonb_typeof(p_case) <> 'object' then
    raise exception 'I dati della pratica non sono validi.'
      using errcode = '22023';
  end if;

  begin
    requested_studio_id := nullif(p_case ->> 'studio_id', '')::uuid;
    requested_client_id := nullif(p_case ->> 'client_contact_id', '')::bigint;
  exception
    when invalid_text_representation then
      raise exception 'Studio o cliente non validi.' using errcode = '22023';
  end;

  if requested_studio_id is null or requested_client_id is null then
    raise exception 'Studio e cliente sono obbligatori.'
      using errcode = '22023';
  end if;

  if p_case_id is null then
    select current_profile.studio_id
    into authorized_studio_id
    from public.profiles as current_profile
    join public.user_permissions as current_permissions
      on current_permissions.user_id = current_profile.id
     and current_permissions.can_edit_cases = true
    where current_profile.id = auth.uid()
      and current_profile.active = true
      and current_profile.deleted_at is null;

    if authorized_studio_id is null
      or authorized_studio_id is distinct from requested_studio_id
    then
      raise exception 'Creazione della pratica non autorizzata per questo studio.'
        using errcode = '42501';
    end if;
  else
    select current_case.studio_id, current_case.client_contact_id
    into authorized_studio_id, existing_client_id
    from public.cases as current_case
    join public.profiles as current_profile
      on current_profile.studio_id = current_case.studio_id
     and current_profile.id = auth.uid()
     and current_profile.active = true
     and current_profile.deleted_at is null
    join public.user_permissions as current_permissions
      on current_permissions.user_id = current_profile.id
     and current_permissions.can_edit_cases = true
    where current_case.id = p_case_id
      and current_case.deleted_at is null;

    if authorized_studio_id is null then
      raise exception 'Pratica non trovata o modifica non autorizzata.'
        using errcode = '42501';
    end if;

    if requested_studio_id is distinct from authorized_studio_id then
      raise exception 'Non è possibile spostare una pratica tra studi.'
        using errcode = '42501';
    end if;
  end if;

  if not exists (
    select 1
    from public.contacts as requested_client
    where requested_client.id = requested_client_id
      and requested_client.studio_id = authorized_studio_id
      and (
        requested_client.deleted_at is null
        or (
          p_case_id is not null
          and requested_client.id = existing_client_id
        )
      )
  ) then
    raise exception
      'Il cliente non esiste, è eliminato o appartiene a un altro studio.'
      using errcode = '22023';
  end if;

  if p_case_id is null and exists (
    select 1
    from unnest(coalesce(p_counterparty_ids, '{}'::bigint[]))
      as requested(requested_id)
    left join public.counterparties as requested_counterparty
      on requested_counterparty.id = requested_id
     and requested_counterparty.studio_id = authorized_studio_id
     and requested_counterparty.deleted_at is null
    where requested_id is null or requested_counterparty.id is null
  ) then
    raise exception
      'Una o più controparti non esistono, sono eliminate o appartengono a un altro studio.'
      using errcode = '22023';
  end if;

  if p_case_id is null then
    insert into public.cases (
      studio_id,
      client_contact_id,
      counterparty_id,
      title,
      case_type,
      claimant_name_raw,
      defendant_name_raw,
      court_type,
      court_city,
      section,
      rg_number,
      judge_name,
      status,
      opening_date,
      description,
      notes,
      needs_review,
      active
    )
    values (
      authorized_studio_id,
      requested_client_id,
      (coalesce(p_counterparty_ids, '{}'::bigint[]))[1],
      nullif(p_case ->> 'title', ''),
      coalesce(nullif(p_case ->> 'case_type', ''), 'causa_lavoro'),
      nullif(p_case ->> 'claimant_name_raw', ''),
      nullif(p_case ->> 'defendant_name_raw', ''),
      nullif(p_case ->> 'court_type', ''),
      nullif(p_case ->> 'court_city', ''),
      nullif(p_case ->> 'section', ''),
      nullif(p_case ->> 'rg_number', ''),
      nullif(p_case ->> 'judge_name', ''),
      coalesce(nullif(p_case ->> 'status', ''), 'nuova'),
      nullif(p_case ->> 'opening_date', '')::date,
      nullif(p_case ->> 'description', ''),
      nullif(p_case ->> 'notes', ''),
      coalesce((p_case ->> 'needs_review')::boolean, false),
      coalesce((p_case ->> 'active')::boolean, true)
    )
    returning id into saved_case_id;
  else
    update public.cases
    set
      client_contact_id = requested_client_id,
      title = nullif(p_case ->> 'title', ''),
      case_type = coalesce(
        nullif(p_case ->> 'case_type', ''),
        case_type
      ),
      claimant_name_raw = nullif(p_case ->> 'claimant_name_raw', ''),
      court_type = nullif(p_case ->> 'court_type', ''),
      court_city = nullif(p_case ->> 'court_city', ''),
      section = nullif(p_case ->> 'section', ''),
      rg_number = nullif(p_case ->> 'rg_number', ''),
      judge_name = nullif(p_case ->> 'judge_name', ''),
      status = coalesce(nullif(p_case ->> 'status', ''), status),
      opening_date = nullif(p_case ->> 'opening_date', '')::date,
      description = nullif(p_case ->> 'description', ''),
      notes = nullif(p_case ->> 'notes', ''),
      needs_review = coalesce(
        (p_case ->> 'needs_review')::boolean,
        needs_review
      ),
      active = coalesce((p_case ->> 'active')::boolean, active)
    where id = p_case_id
      and studio_id = authorized_studio_id
      and deleted_at is null
    returning id into saved_case_id;

    if saved_case_id is null then
      raise exception 'La pratica non è stata aggiornata.'
        using errcode = 'P0001';
    end if;
  end if;

  -- PostgreSQL esegue l'intera RPC in un'unica transazione: qualsiasi errore
  -- nella sincronizzazione annulla anche l'insert o l'update della pratica.
  perform public.set_case_counterparties(
    saved_case_id,
    coalesce(p_counterparty_ids, '{}'::bigint[])
  );

  return saved_case_id;
exception
  when invalid_text_representation or datetime_field_overflow then
    raise exception 'Uno o più dati della pratica non hanno un formato valido.'
      using errcode = '22023';
end;
$$;

revoke all on function public.save_case_with_counterparties(
  jsonb,
  bigint[],
  bigint
) from public;
grant execute on function public.save_case_with_counterparties(
  jsonb,
  bigint[],
  bigint
) to authenticated;

grant select on public.case_counterparties to authenticated;

commit;
