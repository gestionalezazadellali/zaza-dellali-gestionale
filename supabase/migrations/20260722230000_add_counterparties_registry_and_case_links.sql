-- Preserve every existing relational link. No column on cases is changed.
insert into public.case_counterparties (
  studio_id,
  case_id,
  counterparty_id
)
select
  current_case.studio_id,
  current_case.id,
  current_case.counterparty_id
from public.cases as current_case
join public.counterparties as current_counterparty
  on current_counterparty.id = current_case.counterparty_id
 and current_counterparty.studio_id = current_case.studio_id
where current_case.counterparty_id is not null
on conflict do nothing;

-- Convert raw-only names into reviewable records. Existing counterparties are
-- reused by normalized name and neither legacy field on cases is overwritten.
with raw_candidates as (
  select distinct on (
    current_case.studio_id,
    public.normalize_counterparty_name(current_case.defendant_name_raw)
  )
    current_case.studio_id,
    btrim(current_case.defendant_name_raw) as display_name,
    public.normalize_counterparty_name(current_case.defendant_name_raw)
      as normalized_name
  from public.cases as current_case
  where current_case.counterparty_id is null
    and nullif(btrim(current_case.defendant_name_raw), '') is not null
  order by
    current_case.studio_id,
    public.normalize_counterparty_name(current_case.defendant_name_raw),
    current_case.id
)
insert into public.counterparties (
  studio_id,
  name,
  display_name,
  normalized_name,
  counterparty_type,
  active,
  needs_review
)
select
  candidate.studio_id,
  candidate.display_name,
  candidate.display_name,
  candidate.normalized_name,
  'da_classificare',
  true,
  true
from raw_candidates as candidate
where candidate.normalized_name <> ''
  and not exists (
    select 1
    from public.counterparties as existing_counterparty
    where existing_counterparty.studio_id = candidate.studio_id
      and (
        existing_counterparty.normalized_name = candidate.normalized_name
        or public.normalize_counterparty_name(existing_counterparty.name) =
          candidate.normalized_name
      )
  )
on conflict do nothing;

insert into public.case_counterparties (
  studio_id,
  case_id,
  counterparty_id
)
select
  current_case.studio_id,
  current_case.id,
  matched_counterparty.id
from public.cases as current_case
join lateral (
  select existing_counterparty.id
  from public.counterparties as existing_counterparty
  where existing_counterparty.studio_id = current_case.studio_id
    and (
      existing_counterparty.normalized_name =
        public.normalize_counterparty_name(current_case.defendant_name_raw)
      or public.normalize_counterparty_name(existing_counterparty.name) =
        public.normalize_counterparty_name(current_case.defendant_name_raw)
    )
  order by
    (existing_counterparty.deleted_at is null) desc,
    existing_counterparty.id
  limit 1
) as matched_counterparty on true
where current_case.counterparty_id is null
  and nullif(btrim(current_case.defendant_name_raw), '') is not null
on conflict do nothing;

create or replace function public.enforce_counterparty_soft_delete_permission()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if old.deleted_at is distinct from new.deleted_at
    and auth.uid() is not null
    and not exists (
      select 1
      from public.user_permissions
      where user_id = auth.uid()
        and can_edit_clients = true
    )
  then
    raise exception
      'Non disponi del permesso per eliminare o ripristinare le controparti.'
      using errcode = '42501';
  end if;

  return new;
end;
$$;

drop trigger if exists counterparties_soft_delete_permission
  on public.counterparties;

create trigger counterparties_soft_delete_permission
before update of deleted_at on public.counterparties
for each row
execute function public.enforce_counterparty_soft_delete_permission();

create or replace function public.enforce_counterparty_write_permission()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if auth.uid() is not null
    and not exists (
      select 1
      from public.profiles as current_profile
      join public.user_permissions as current_permissions
        on current_permissions.user_id = current_profile.id
      where current_profile.id = auth.uid()
        and current_profile.studio_id = new.studio_id
        and current_profile.active = true
        and current_profile.deleted_at is null
        and current_permissions.can_edit_clients = true
    )
  then
    raise exception
      'Non disponi del permesso per modificare le controparti di questo studio.'
      using errcode = '42501';
  end if;

  if tg_op = 'UPDATE' and old.studio_id is distinct from new.studio_id then
    raise exception 'Non è possibile spostare una controparte tra studi.'
      using errcode = '42501';
  end if;

  return new;
end;
$$;

drop trigger if exists counterparties_write_permission
  on public.counterparties;

create trigger counterparties_write_permission
before insert or update on public.counterparties
for each row
execute function public.enforce_counterparty_write_permission();

