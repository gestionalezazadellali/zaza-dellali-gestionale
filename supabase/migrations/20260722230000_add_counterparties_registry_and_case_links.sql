create or replace function public.normalize_counterparty_name(p_value text)
returns text
language sql
immutable
parallel safe
as $$
  select btrim(
    regexp_replace(
      translate(
        lower(btrim(coalesce(p_value, ''))),
        'àáâäãåèéêëìíîïòóôöõùúûüç',
        'aaaaaaeeeeiiiiooooouuuuc'
      ),
      '[^a-z0-9]+',
      ' ',
      'g'
    )
  );
$$;

update public.counterparties
set normalized_name = public.normalize_counterparty_name(display_name)
where normalized_name is null or btrim(normalized_name) = '';

